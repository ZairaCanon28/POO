public class declaracionvariables {
    public static void main(String[] args) {
    //Declaración de variables 
        String nombre = "Zaira";
        boolean esempleado = true;
        char incialApellido = 'A';
        int edad = 30;
        double aumento =  1.75;

    //Impresión de variables
        System.out.println("Nombre: "+nombre);
        System.out.println("Inicial de apelido: "+incialApellido);
        System.out.println("¿Es empleado? "+esempleado);
        System.out.println("Edad: "+edad);
        System.out.println("Porcentaje de aumento salarial "+aumento+ "%");

    //Expresión de todo
        System.out.println("El usuario: "+ nombre + ", con inicial de apellido: "+incialApellido+
        ", con edad: "+edad+ " .Es empleado" + esempleado+ " y tiene un aumento salaral del: "+aumento+ "%");
    }
}

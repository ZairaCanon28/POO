public class definciones {
    public static void main(String[] args) {
        // Declaración de variables con identificadores válidos
        int myVariable = 10;
        double _privateVar = 20.5;
        float $amount = 1000.75f;
        char age2 = 'A';
        boolean MAX_VALUE = true;

        // Imprimir los valores de las variables
        System.out.println("Valor de myVariable: " + myVariable);
        System.out.println("Valor de _privateVar: " + _privateVar);
        System.out.println("Valor de $amount: " + $amount);
        System.out.println("Valor de age2: " + age2);
        System.out.println("Valor de MAX_VALUE: " + MAX_VALUE);
    }
}
